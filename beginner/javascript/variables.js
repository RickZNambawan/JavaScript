/* old way of declaring variables */
var score = 75;
console.log("Score: ", score);

/* new way of declaring variable */
// let - if you want this variable to change the value in the future
let age = 25;
let year = 2019;
console.log("Age: ", age, "\n" + "Year: ", year);

/* const - constant variable and will not change the value of the variable */
const points = 100;
console.log("Points: ", points); 

/* override variable */
age = 30;
console.log("New Age: ", age);
